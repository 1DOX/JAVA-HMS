    private void PayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PayButtonActionPerformed
        // TODO add your handling code here:
        String room_num = RoomNumField.getText();
        
        dbLoad();
        String card_num = Card_num(room_num);
        int pay = getfoodint("foodsystem", "room_num", room_num, "food_total_pay");
        
        if(!room_num.isEmpty() && search_roomnum_2(room_num,"reservation")!=null)
        {
            if(pay != 0)
          {
            if(choice == 0) JOptionPane.showMessageDialog(this, "결제수단을 선택해주세요.", "메세지", JOptionPane.INFORMATION_MESSAGE);
            else if(choice== 1)
            {
                dispose();
                new BillCard(card_num, room_num, 2).setVisible(true);
            }
            else if(choice == 2)
            {
                dispose();
                new BillCash(room_num, 2).setVisible(true);
            }
            
         }
        else JOptionPane.showMessageDialog(this, "결제할 금액이 없습니다.", "메세지", JOptionPane.INFORMATION_MESSAGE);
        }
        else if(search_roomnum_2(room_num,"reservation") == null)
        {
            JOptionPane.showMessageDialog(this, "일치하는 방번호가 없습니다.", "메세지", JOptionPane.INFORMATION_MESSAGE);
        }
        else JOptionPane.showMessageDialog(this, "방번호를 입력해주세요.", "메세지", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_PayButtonActionPerformed

