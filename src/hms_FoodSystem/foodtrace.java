    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        dbLoad();
        
        String[] mealname = new String[6];
        String[] mealname2 = new String[3];
        String[] mealname3 = new String[3];
        int[] mealnum = new int[6];
        int[] mealnum2 = new int[3];
        int[] mealnum3 = new int[3];
        String result = "";
        int resultpay = 0;
        String c = RoomNumField.getText();
        if(search_roomnum(c) != null)  
        {
        for(int i=0; i<6; i++)
        {
            mealname[i] = getfoodString("hmsfood", "foodtype", "1", "food_"+(i+1));
            mealnum[i] = getfoodint("foodsystem", "room_num", c, "food_"+(i+1)+"_num");
            if(!getfoodString("hmsfood", "foodtype", "1", "food_"+(i+1)).isEmpty())
            {
                int charge = getfoodint("foodsystem", "room_num", c, "food_"+(i+1)+"_charge");
                int pay = getfoodint("hmsfood", "foodtype", "1", "food_"+(i+1)+"_pay");
                result += mealname[i] + " " + charge + "개\n";
                resultpay += charge * pay;
            }
        }
        for(int i=0; i<3; i++)
        {
            mealname2[i] = getfoodString("hmsfood", "foodtype", "2", "food_"+(i+1));
            mealnum2[i] = getfoodint("foodsystem", "room_num", c, "food_service_"+(i+1));
            if(!getfoodString("hmsfood", "foodtype", "2", "food_"+(i+1)).isEmpty())
            {
                result += mealname2[i] + " 코스 " + mealnum2[i] + "개\n";
                resultpay += mealnum2[i] * getfoodint("hmsfood", "foodtype", "2", "food_"+(i+1)+"_pay");
            }
        }
        for(int i=0; i<3; i++)
        {
            mealname3[i] = getfoodString("hmsfood", "foodtype", "3", "food_"+(i+1));
            mealnum3[i] = getfoodint("foodsystem", "room_num", c, "food_service_"+(i+3));
            if(!getfoodString("hmsfood", "foodtype", "3", "food_"+(i+1)).isEmpty())
            {
                result += mealname3[i] + " 코스 " + mealnum3[i] + "개\n";
                resultpay += mealnum3[i] * getfoodint("hmsfood", "foodtype", "3", "food_"+(i+1)+"_pay");
            }
        }
        Updatechange("foodsystem", "food_total_pay", "room_num", RoomNumField.getText(), resultpay);
        result += "총액: "+ getfoodint("foodsystem", "room_num", RoomNumField.getText(), "food_total_pay");
            if(!mealname[0].isEmpty()) Menu1Field.setText(mealname[0] + "(" + mealnum[0]+")");
            if(!mealname[1].isEmpty()) Menu2Field.setText(mealname[1]+ "(" + mealnum[1]+")");
            if(!mealname[2].isEmpty()) Menu3Field.setText(mealname[2]+ "(" + mealnum[2]+")");
            if(!mealname[3].isEmpty()) Menu4Field.setText(mealname[3]+ "(" + mealnum[3]+")");
            if(!mealname[4].isEmpty()) Menu5Field.setText(mealname[4]+ "(" + mealnum[4]+")");
            if(!mealname[5].isEmpty()) Menu6Field.setText(mealname[5]+ "(" + mealnum[5]+")");
            BillTextArea.setText(result);
        }
        else JOptionPane.showMessageDialog(this, "일치하는 방번호가 없습니다.", "메세지", JOptionPane.INFORMATION_MESSAGE);
        dbClose();
    }//GEN-LAST:event_SearchButtonActionPerformed

